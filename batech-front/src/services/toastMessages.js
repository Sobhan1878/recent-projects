import { toast } from "react-toastify";

export const infoToast = (message) => {
    toast.info(message, {
        position: "bottom-center",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "dark",
        toastId: "customToast",
    });
};

